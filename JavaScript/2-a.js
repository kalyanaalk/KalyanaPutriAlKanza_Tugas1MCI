const penjumlahan = function(a, b) {
    return a + b;
  };
  
  const pengurangan = function(a, b) {
    return a - b;
  };
  
  const perkalian = function(a, b) {
    return a * b;
  };
  
  const pembagian = function(a, b) {
    return a / b;
  };
  
  module.exports = {
    penjumlahan,
    pengurangan,
    perkalian,
    pembagian,
  };  