--    2 juta, maka masuk golongan "B" Jika ukt mahasiswa diatas 2 juta, maka masuk golongan "C".

ALTER TABLE mytable
ADD golongan char(1);

UPDATE mytable
SET golongan = 'A'
WHERE ukt <= 1000000;

UPDATE mytable
SET golongan = 'B'
WHERE ukt > 1000000 AND ukt < 2000000;

UPDATE mytable
SET golongan = 'C'
WHERE ukt >= 2000000;

SELECT nama, ukt, golongan FROM mytable;

-- 5. Tampilkan daftar nama, semester dan ipk berdasarkan ipk tertinggi tiap semester. 
--    Diurutkan berdasarkan semesternya. Jika ada yang nilainya sama, maka diurutkan 
--    berdasarkan alfabet.

SELECT mt1.nama, mt1.semester, mt1.ipk
FROM mytable mt1
WHERE mt1.ipk = (
    SELECT MAX(mt2.ipk)
    FROM mytable mt2
    WHERE mt2.semester = mt1.semester
)
ORDER BY mt1.semester ASC, mt1.nama ASC;